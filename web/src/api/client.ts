import createClient, { type Middleware } from "openapi-fetch";
import type { paths } from "./schema.gen";

// The bearer token the console authenticates with. Our backend is bearer-only
// (Slice 2): there is no session cookie yet, so the SPA holds the token in
// localStorage and attaches it to every request. Mint one with
// `omniglass bootstrap <username>`; paste it on the login screen.
const TOKEN_KEY = "og-token";

export function getToken(): string {
  return localStorage.getItem(TOKEN_KEY) ?? "";
}
export function setToken(token: string): void {
  localStorage.setItem(TOKEN_KEY, token);
}
export function clearToken(): void {
  localStorage.removeItem(TOKEN_KEY);
}

// authMiddleware attaches the stored bearer token to every outbound request.
const authMiddleware: Middleware = {
  async onRequest({ request }) {
    const token = getToken();
    if (token) request.headers.set("Authorization", `Bearer ${token}`);
    return request;
  },
};

// Typed client over the Huma API. Operations live under /api/v1 (the OpenAPI
// server base); the SPA is same-origin (served by the Go binary), and under
// `npm run dev` Vite proxies /api to the running server. The base is made
// origin-absolute so it resolves in every fetch implementation (the browser, and
// undici in tests). Types come from schema.gen.ts (regenerated by `make gen`),
// so the client cannot drift.
const origin = typeof globalThis.location !== "undefined" ? globalThis.location.origin : "";
export const api = createClient<paths>({
  baseUrl: `${origin}/api/v1`,
  // Defer to the current global fetch on every call rather than capturing it at
  // client-construction, so tests can stub globalThis.fetch.
  fetch: (input) => globalThis.fetch(input),
});
api.use(authMiddleware);
